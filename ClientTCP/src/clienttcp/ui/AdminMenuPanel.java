    private void initData() {
        model = new javax.swing.table.DefaultTableModel(
                new Object[]{"ID", "Nama", "Kategori", "Harga", "Deskripsi", "Tersedia"}, 0);
        tableMenu.setModel(model);

        tableMenu.getSelectionModel().addListSelectionListener(e -> {
            int row = tableMenu.getSelectedRow();
            if (row != -1) {
                txtName.setText(model.getValueAt(row, 1).toString());
                txtCategory.setText(model.getValueAt(row, 2).toString());
                txtPrice.setText(model.getValueAt(row, 3).toString());
                txtDesc.setText(model.getValueAt(row, 4).toString());
            }
        });

        loadMenu();
    }

    private void loadMenu() {
        try { // Call Web Service Operation
            clienttcp.wsclient.UASWebService_Service service = new clienttcp.wsclient.UASWebService_Service();
            clienttcp.wsclient.UASWebService port = service.getUASWebServicePort();
            java.util.List<clienttcp.wsclient.MenuItem> list = port.getAllMenu();

            model.setRowCount(0);
            if (list != null) {
                for (clienttcp.wsclient.MenuItem m : list) {
                    model.addRow(new Object[]{m.getId(), m.getName(), m.getCategory(),
                        m.getPrice(), m.getDescription(), m.isAvailable()});
                }
            }
        } catch (Exception ex) {
            showError(ex);
        }
    }

    private void showError(Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal terhubung ke Web Service.\n" + ex.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
    }

