    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tableMenu.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih menu yang ingin diupdate");
            return;
        }
        try {
            int id = (int) model.getValueAt(row, 0);
            double price = Double.parseDouble(txtPrice.getText().trim());

            clienttcp.wsclient.UASWebService_Service service = new clienttcp.wsclient.UASWebService_Service();
            clienttcp.wsclient.UASWebService port = service.getUASWebServicePort();
            java.lang.String resp = port.updateMenu(id, txtName.getText().trim(), txtCategory.getText().trim(),
                    price, txtDesc.getText().trim(), true);

            javax.swing.JOptionPane.showMessageDialog(this, resp);
            loadMenu();
        } catch (NumberFormatException nfe) {
            javax.swing.JOptionPane.showMessageDialog(this, "Harga harus berupa angka");
        } catch (Exception ex) {
            showError(ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tableMenu.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih menu yang ingin dihapus");
            return;
        }
        try {
            int id = (int) model.getValueAt(row, 0);

            clienttcp.wsclient.UASWebService_Service service = new clienttcp.wsclient.UASWebService_Service();
            clienttcp.wsclient.UASWebService port = service.getUASWebServicePort();
            java.lang.String resp = port.deleteMenu(id);

            javax.swing.JOptionPane.showMessageDialog(this, resp);
            loadMenu();
        } catch (Exception ex) {
            showError(ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

