    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tableOrders.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih order yang ingin diupdate");
            return;
        }
        try {
            String orderId = model.getValueAt(row, 0).toString();
            String status = (String) comboStatus.getSelectedItem();
            String resp = tcp.sendCommand("UPDATE_ORDER_STATUS|" + orderId + "|" + status);
            javax.swing.JOptionPane.showMessageDialog(this, resp);
            loadOrders();
        } catch (Exception ex) {
            showError(ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

