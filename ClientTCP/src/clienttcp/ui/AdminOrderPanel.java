    public AdminOrderPanel(TCPClient tcp) {
        this();
        this.tcp = tcp;
        model = new javax.swing.table.DefaultTableModel(
                new Object[]{"Order ID", "Reservation ID", "Status", "Total"}, 0);
        tableOrders.setModel(model);
        loadOrders();
    }

    private void loadOrders() {
        try {
            String resp = tcp.sendCommand("LIST_ORDERS");
            model.setRowCount(0);
            if (resp != null && resp.startsWith("OK")) {
                String[] parts = resp.split("\\|");
                for (int i = 1; i < parts.length; i++) {
                    String[] f = parts[i].split(":");
                    model.addRow(new Object[]{f[0], f[1], f[2], f[3]});
                }
            }
        } catch (Exception ex) {
            showError(ex);
        }
    }

    private void showError(Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal terhubung ke Server TCP.\n" + ex.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
    }

